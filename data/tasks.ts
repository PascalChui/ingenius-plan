// Helper function to get tasks for a team
export function getTasksForTeam(teamId: string): Task[] {
  return tasks.filter((task) => task.teamId === teamId)
}

// Helper function to get tasks assigned to a user
export function getTasksAssignedToUser(userId: string): Task[] {
  return tasks.filter((task) => task.assignee === userId)
}

// Helper function to get tasks created by a user
export function getTasksCreatedByUser(userId: string): Task[] {
  return tasks.filter((task) => task.creator === userId)
}

// Helper function to get tasks where user is a collaborator
export function getTasksCollaboratingOn(userId: string): Task[] {
  return tasks.filter(
    (task) =>
      task.collaborators?.includes(userId) ||
      task.watchers?.includes(userId) ||
      task.comments?.some((comment) => comment.mentions?.includes(userId)),
  )
}

export function getTasks(): Task[] {
  return tasks
}
